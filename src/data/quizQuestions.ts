// RANDOM SELECTION FUNCTION
export const getRandomQuestionsForAssessment = (): QuizQuestion[] => {
  const beginnerQuestions = questionBank.filter(q => q.difficulty === 'A1' || q.difficulty === 'A2');
  const intermediateQuestions = questionBank.filter(q => q.difficulty === 'B1' || q.difficulty === 'B2');
  const advancedQuestions = questionBank.filter(q => q.difficulty === 'C1' || q.difficulty === 'C2');

  // Shuffle and select 5 from each category
  const selectedBeginner = shuffleArray(beginnerQuestions).slice(0, 5);
  const selectedIntermediate = shuffleArray(intermediateQuestions).slice(0, 5);
  const selectedAdvanced = shuffleArray(advancedQuestions).slice(0, 5);

  // Combine and shuffle the final 15 questions
  return shuffleArray([...selectedBeginner, ...selectedIntermediate, ...selectedAdvanced]);
};

// Fisher-Yates shuffle algorithm
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

